export function getToolsByCategory(category: ToolCategory): CompetitiveIntelligenceTool[] {
  return competitiveIntelligenceTools.filter(tool => tool.category === category);
}

export function searchTools(query: string): CompetitiveIntelligenceTool[] {
  const lowerQuery = query.toLowerCase();
  return competitiveIntelligenceTools.filter(
    tool => 
      tool.name.toLowerCase().includes(lowerQuery) ||
      tool.description.toLowerCase().includes(lowerQuery) ||
      tool.features?.some(f => f.toLowerCase().includes(lowerQuery))
  );
}
